		<div><h2><center>Wedo Entertainment Community Guidelines</center></h2></div>
		
		<p>This document, formulated in accordance with the Wedo Entertainment Community Guidelines, is to strengthen
the management and regulation on short videos and LIVE broadcast content, providing Wedo Entertainment users
with a healthy and harmonious platform to display and appreciate talents.
Definition of incompliant content: all short video/LIVE broadcast contents and comments should be
strictly in compliance with provisions of the Wedo Entertainment Community Guidelines, and certain rules shall be
referred to as appropriate. Please see the Wedo Entertainment Community Guidelines for further information.
Videos and LIVE broadcasts containing the following contents are strictly prohibited from
being uploaded:</p>
<p>1. Any speech against the country, the government, irreligious speech, or acts which insult or
slander the government, the country or religions, for example:
Endangering the national security, divulging national secrecy, threatening to overturn state power, or
undermining national unity;</p>
<p>Inciting national hatred, ethnic discrimination, or undermining national unity;
Spreading violence, terrorism or abet crimes;
Inciting illegal assembly or formation gathering to disturb social order;
Undermining national religious policy or advocating heresy and feudalistic superstition.</p>
<p>2. Videos containing contents of sexual behavior performance or pornographic content are
prohibited from spreading and playing.</p>
<p>3. Any behaviors that expose private parts are prohibited, such as showing female’s breast, male or
female’s buttocks, inner thigh or reproductive organs, wearing transparent clothes, or female not
wearing underpants.</p>
<p>4. It is prohibited to spread or play gambling-related content. Behaviors such as broadcasting
gambling or inciting others to gamble are not allowed.</p>
<p>5. It is prohibited to expose others’ privacy, pose threat to others’ safety, imperil public interests,
injure or kill others, mistreat animals, display bloody or violent behaviors, demonstrate guns or dress
up like a terrorist.</p>
<p>6. Spreading drugs is prohibited. It is not allowed to demonstrate drug samples, perform drug taking,
spread drug use or injection methods, or introduce ways to make drugs.</p>
<p>7. It is prohibited to damage the interest of the official platform, including behaviors like promoting
competitive products, viciously soliciting and publishing fake ads.</p>
<p>8. Knives or simulated knives that may harm life and health are prohibited from being used in videos.
It is prohibited to perform extremely dangerous shows.</p>
<p>9. It is prohibited to infringe others’ legal rights, such as:
Exposing others’ privacy, including names, addresses, phone numbers and other private
information; Pirating or recording any unauthorized contents, including videos containing ads, watermarks or
names of other competitive software products;</p>
<p>Videos and LIVE broadcasts containing the following contents might result in video removal
or certain penalties, including but not limited to:</p>
<p>1. Short videos/LIVE broadcasts with a pure-colored cover or dim cover.</p>
<p>2. Uploading videos that are too short (less than 5 seconds).</p>
<p>3. LIVE broadcasts that are too short. The recommended LIVE duration is 30+ minutes per session.</p>
<p>4. Videos/LIVE broadcasts without any quality contents, such as static pictures, vague pictures, dim light or randomly taken videos</p>
<p>5. Any content that may make viewers uncomfortable, such as vulgar, horrific and terrifying videos.
Definition of incompliant video/LIVE broadcast themes and comments, including but not
limited to:</p>
<p>1. Inciting behaviors that may disturb social order, including regional assault, religious abuse and
personal insult</p>
<p>2. Any political topics that may cause or have caused bad influence.</p>
<p>3. Comments containing promotional commercial ads.</p>
<p>Users conducting the behaviors above shall be prohibited from using certain functions and
interacting with others. As to users with severe violations or breaking the rules repeatedly, their
accounts might be permanently banned and they will no longer be able to use Wedo Entertainment.
Wedo Entertainment reserves the final right of interpretation of this document, and Wedo Entertainment has the right to change
and publish this document.</p>
<p>This document shall come into force as of the date of promulgation.</p>
