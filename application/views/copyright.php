<p>Counter-Notification Process</p>
<p>If WEDO ENTERTAINMENT receives a valid notification of alleged copyright infringement (the
“Notification”), WEDO ENTERTAINMENT will take reasonable steps to contact the user that
uploaded to APP Wedo Entertainment the relevant allegedly infringing material (the "Material"); and the user may
then submit a valid counter-notification in relation to the Notification (the "Counter-Notification").</p>
<p>During the process, WEDO ENTERTAINMENT would remove the infringing work or disable access
to the work in question until the dispute is resolved.</p>
<p>Please note that WEDO ENTERTAINMENT may provide copies of any Notifications or CounterNotifications to any person where required by law or as otherwise reasonably required by us to
protect our interests.</p>
<p>A counter-notification must include the following information of the user,</p>
<p>1. The contact information, including full legal name (not that of a company), an email address, a
physical address, and a phone number. An authorized representative of the user such as an
attorney shall specify the relationship between the representatives and represented.
</p>
<p>2. Identify the location, including any URL, of the work that has been removed or disabled.</p>
<p>3. The User shall agree to and include the following statement: "I consent to the jurisdiction of the
