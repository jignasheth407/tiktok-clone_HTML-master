<p>2. A clear and complete description of the work that you believe has been infringed.</p>
<p>3. The URL or other identifying location of the allegedly infringing work.</p>
<p>4. A statement that the copyright owner has a good faith belief that use of the work in the manner
complained of is not authorized by the copyright owner, its agent, or law</p>
<p>5. A statement made under penalty of perjury, that the above information is accurate, and in the
